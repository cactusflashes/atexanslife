            <p>
                In September of 1957, Juan & his family reloacted to Douglas, Arizona. Not long after, Juan met his 
                life-long friend Joe Duarte, bonding over guitar and wrestling. 
            </p>
            <p>
                Juan soon became enamored by one of his female classmates. 
                <strong>[Ginny] immediately became the apple, banana, and nectarine cider of my eye.</strong>"
            </p>
            <div style="height: 15px"></div>
