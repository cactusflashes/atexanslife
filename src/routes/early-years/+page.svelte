                <strong> The University of Texas, Rio Grande Valley</strong>. 
            </p>
            <p>
                In his youth, Juan grew up playing with neighborhood friends and working miscellaneous 
                jobs in and around Texas, including traversing the state picking cotton or tomatoes as young
                as four years old. 
            </p>
            <p style="opacity: 0.6">
                <em>Brownsville was experiencing an agricultural boom during this time, at one point even 
                becoming the world's leading exporter of cotton in the 1950's for a brief stint.<sup>1</sup> </em>
            </p>
            <p>
                In 1957, Juan's father recieved a letter from his brother Tony, who had returned from the Army and headed south to find work. 
                Tony found work as a construction union president in Douglas, Arizona. With the prospect of 
                better work on the horizon, Juan's parents made the decision to relocate.
            <p style="opacity: 0.6">
                <em>
                    Juan's parents had previously lived in Pirtleville, Arizona for a brief period in 1944 before Juan's birth. 
                    During this time, Juan's father worked laying railroad tracks. 
                </em>
            </p>
            <div style="height: 15px"></div>
            <img src={douglas} class="page-img" alt="Douglas, AZ in the late 1950's"/>
