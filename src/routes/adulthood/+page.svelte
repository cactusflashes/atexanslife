             <p>
                Aboard the Kearsarge, Juan worked manning the phones to identify and report damage that was caused by
                the possibility of an attack from enemy weapons. Juan's final position in the Navy was as a Yeoman 
                Third Class, assisting with administrative duties aboard the ship. 
            </p>
            <p>
                During his time in the Navy, Juan's first daughter Barbara (Barbie) was born on November 8th, 1965. Not long after, 
                Juan was honorably dischared from the Navy, and began looking around 
                California for work—eventually finding a job at an aluminum factory in Torrence, California. 
            </p>
            <div style="height: 15px"></div>
            <img src={jpsmall} alt="Juan and Barbie in the backyard." class="page-img"/>
            <p style="font-size: .8rem; opacity: 0.5; margin: 10; width: 90%;">
                Juan with his baby daughter, Barbara. 
             </p>
            <p>
                After working at the aluminium factory for a period of time, Juan and Virginia grew homesick and made the 
                decision to move back to Douglas, Arizona. They arrived back in Douglas in October of 1967. Immediately after, 
                Juan accepted a job working for the City Loan & Finance Company. He began as a collector, eventually moving into 
                the position of Loan Officer. 
            </p>
            <p>
                In 1969, Juan was hired at Valley National Bank, assuming the role of Collector, then Loan Officer, 
                and eventually Manager of the loan department. Juan oversaw ten employees, and was responsible for 
                a loan portfolio of over 12 million dollars. 
            </p>
            <p>
                On September 22, 1968, Juan and Virginia's second child, Christy was born, followed by Johnny in 1975.
            </p>
            <p>
                In 1989, Valley National Bank merged with a Chicago-based bank, and laid off a large percentage of the 
                workforce, including Juan. Juan looked for work in Douglas, Sierra Vista, and Tucson. Eventually, Juan was
                hired at the Arizona-Mexico Border Health Foundation, where he worked to educate young adults on the 
                consequences of drugs, alcohol, and tobacco. 
            </p>
            <p>
                Juan would chaperone children between the ages of eight and twelve, taking them fishing while educating them on the harmful effects
                of drugs along the way. 
            </p>
            <p>
                In October of 1997, Juan was hired as a manager and loan officer at Southeastern Arizona Federal Credit Union, where
                he remained until his retirement.  
            </p>
            <p>
                Shortly before his retirement from the credit union, Juan formed a band with a group of friends, known
                as "Blind Dawg". 
            </p>
