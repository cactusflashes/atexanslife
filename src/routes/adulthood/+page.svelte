        <p>
            Juan and Virginia married on January 30th, 1965. Directly after
            their ceremony, they headed to Long Beach by bus to begin the next
            part of their lives together.
        </p>

        <img src={balc} alt="Juan's navy portrait" class="page-img-vert" />
        <div class="img-caption">Juan and Virgina on their wedding day.</div>

        <p>
            After moving to Long Beach, Juan was stationed aboard the USS
            Kearsarge (CVS-33), an aircraft carrier that the Navy converted into
            an anti-submarine warfare carrier, or ASW carrier. The Kearsarge
            embarked on several journies during the Vietnam and Korean war,
            including areas of the South China Sea, Hawaii, Japan, and Malaysia.
        </p>

        <img src={carrier} alt="Juan's navy portrait" class="page-img-vert" />
