            <p>
                Beginning in 1962, Juan joined the US Navy Reserves while still in his junior year of highschool.
                During this time, he would commute between Douglas and Long Beach, California for basic training. 
            </p>
            <p>
                Shortly prior to his graduation, Juan and Virginia, who had been dating for a few years prior, 
                made the decision to get engaged. Juan sold his Fender Stratocaster guitar to pay for their engagement ring.
            </p>
            <div style="height: 15px"></div>
            <h5>
                "Nowadays, I look at a picture of my guitar, then look at Ginny [and back] at the guitar. Then I thank God. My wife,
