            <h1 style="margin-bottom: 20px;">Obituary</h1>
            <p>
                Juan M. Perez, our beloved husband and father, peacefully passed
                away at his home on December 6, 2023, surrounded by his loved
                ones. Born in Brownsville, Texas in 1945, Johnny, also known as
                the “Juan and only”, to his friends and family, briefly resided
                in Tucson before settling in Douglas. Our Dad first met our
                mother, Virginia, during their middle school years at 12th
                Street Junior High and they later married in 1965. He joined the
                US Navy Reserves while still in high school in 1962. Upon
                graduating from DHS in 1964, he was stationed in Long Beach,
                California on the USS Kearsarge, carrying out missions on the
                outskirts of Vietnam waters.
            </p>
            <p>
                After his military service, he returned to Douglas with his
                family and began working for the Savings and Loan Company. He
                later worked at Valley National Bank, where he dedicated over 25
                years of his life. Subsequently, he worked for Compañeros and
                Southeastern Federal Credit Union. Following his retirement from
                the credit union, he continued to serve on their board. Johnny
                was a true advocate for making Douglas a better community. He
                oversaw the Jerry Lewis Telethon (Muscular Dystrophy) in Douglas
                and was an active member of the Noon Lions Club, Salvation Army,
                and Wall of Faces. Johnny was a true family man, his greatest
                love was our mother, his children and grandchildren. He was a
                friend to all and his absence will be deeply felt.
            </p>
            <p>
                His passing is preceded by his parents Juan R. and Margarita
                Perez and Sister, Eva Gomez. He is survived by his wife,
                Virginia, of 58 years, his children, Barbara Salmon, Christy
                Camargo and Johnny Perez, 9 grandchildren, 4 great grandchildren
                and his sisters, Dolores Stuppi, Maggie Tamayo and Frances Hill.
            </p>
